    for i in range(len(pieceArray)):
        if pieceArray[i].weight == minWeight:
            newValue = calcArmorValue(launchValues, pieceArray[i])
            if newValue > bestValue:
                bestInitPiece = pieceArray[i]
    return bestInitPiece


